places = [
    'germany', 'france', 'japan', 'italy', 'greece'
]

print(places)

sorted_places = sorted(places)
print(f'\n{sorted_places}')

print(f'\n{places}')

print(f'\n{sorted(places, reverse=True)}')

print(f'\n{places}')

places.reverse()
print(f'\n{places}')

places.reverse()
print(f'\n{places}')

places.sort()
print(f'\n{places}')

places.sort(reverse=True)
print(f'\n{places}')

